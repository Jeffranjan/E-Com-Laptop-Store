const Product = require("../../models/Product");

const getFilteredProducts = async (req, res) => {
  try {
    const { category = [], brand = [], sortBy = "price-lowtohigh" } = req.query;

    let filters = {};

    if (category && category.length > 0) {
      filters.category = { $in: category.split(",") };
    }

    if (brand && brand.length > 0) {
      // Convert brand filter to proper case for matching
      const brandValues = brand.split(",").map((b) => {
        const lowerBrand = b.toLowerCase();
        if (lowerBrand === "hp") return "HP";
        if (lowerBrand === "dell") return "Dell";
        if (lowerBrand === "lenovo") return "Lenovo";
        if (lowerBrand === "asus") return "Asus";
        if (lowerBrand === "apple") return "Apple";
        return b; // Return original if no match
      });
      filters.brand = { $in: brandValues };
    }

    let sort = {};

    switch (sortBy) {
      case "price-lowtohigh":
        sort.price = 1;

        break;
      case "price-hightolow":
        sort.price = -1;

        break;
      case "title-atoz":
        sort.title = 1;

        break;

      case "title-ztoa":
        sort.title = -1;

        break;

      default:
        sort.price = 1;
        break;
    }

    const products = await Product.find(filters).sort(sort);

    res.status(200).json({
      success: true,
      data: products,
    });
  } catch (e) {
    console.log(e);
    res.status(500).json({
      success: false,
      message: "Some error occurred",
    });
  }
};

const getProductDetails = async (req, res) => {
  try {
    const { id } = req.params;
    const product = await Product.findById(id);

    if (!product)
      return res.status(404).json({
        success: false,
        message: "Product not found!",
      });

    res.status(200).json({
      success: true,
      data: product,
    });
  } catch (e) {
    console.log(e);
    res.status(500).json({
      success: false,
      message: "Some error occurred",
    });
  }
};

module.exports = { getFilteredProducts, getProductDetails };
